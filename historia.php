            </div>
            <p>
            El <strong>poque</strong> francés llegó a América con los colonos franceses en Luisiana a finales del siglo XVIII y principios del XIX, especialmente en Nueva Orleans. Allí se adoptaron las cartas con picas, corazones, tréboles y diamantes, y el nombre evolucionó a <strong>poker</strong> en inglés. El juego se expandió rápidamente por Estados Unidos, ayudado por los barcos de vapor en los ríos Mississippi y Ohio, y posteriormente por el ferrocarril y las carreteras.
