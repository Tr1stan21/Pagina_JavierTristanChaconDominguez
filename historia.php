            </div>
            <p>
            Durante la Edad Media, el comercio entre Europa y Asia facilitó la llegada de juegos de cartas a Europa. A través de rutas como la de la seda, y gracias a comerciantes y cruzados, estos juegos llegaron a Italia, España, Francia y Alemania. En el siglo XV surgen variantes como el <strong>flusso</strong> en Italia y el <strong>brelan</strong> en Francia. En el siglo XVI, juegos como el <strong>primero</strong> o <strong>la prime</strong> ganan popularidad en el sur de Europa. En el siglo XVII, en Francia surge el <strong>poque</strong> y en Alemania el <strong>pochen</strong>, ambos incorporando apuestas y el arte del farol.
