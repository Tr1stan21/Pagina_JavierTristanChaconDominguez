            Durante el siglo XIX, el poker se convirtió en símbolo de la cultura estadounidense, especialmente en el contexto de la fiebre del oro y la expansión hacia el oeste. Salones y campamentos mineros se llenaban de partidas emocionantes, y surgieron leyendas como <strong>Wild Bill Hickok</strong> y <strong>Doc Holliday</strong>. En 1834, Jonathan H. Green estableció las primeras reglas formales y consolidó el nombre "poker".
            </p>
            <p>
